  async getAllServices(): Promise<AwsService[]> {
    return Array.from(this.services.values());
  }

  async getServicesByCategory(category: string): Promise<AwsService[]> {
    return Array.from(this.services.values()).filter(
      service => service.category.toLowerCase() === category.toLowerCase()
    );
  }

  async searchServices(query: string): Promise<AwsService[]> {
    const searchTerm = query.toLowerCase();
    return Array.from(this.services.values()).filter(
      service => 
        service.name.toLowerCase().includes(searchTerm) ||
        service.fullName.toLowerCase().includes(searchTerm) ||
        service.category.toLowerCase().includes(searchTerm)
    );
  }

  async createService(insertService: InsertAwsService): Promise<AwsService> {
    const id = this.currentId++;
    const service: AwsService = { 
      ...insertService, 
      id,
      certificationTracks: insertService.certificationTracks ?? [],
      status: insertService.status ?? "operational",
      lastStatusCheck: new Date()
    };
    this.services.set(id, service);
    return service;
  }

  async updateServiceStatus(serviceId: number, status: string): Promise<AwsService | null> {
    const service = this.services.get(serviceId);
    if (!service) return null;
    
    const updatedService = {
      ...service,
      status,
      lastStatusCheck: new Date()
    };
    
    this.services.set(serviceId, updatedService);
    return updatedService;
  }

  async getServiceStatus(serviceId: number): Promise<{ status: string; lastStatusCheck: Date } | null> {
    const service = this.services.get(serviceId);
    if (!service) return null;
    
    return {
      status: service.status || "operational",
      lastStatusCheck: service.lastStatusCheck || new Date()
    };
  }
